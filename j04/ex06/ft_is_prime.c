int	ft_is_prime(int nb)
{
	int flag;
	int n;

	n = nb - 1;
	flag = 0;
	if (nb < 2)
		return (0);
	while (n >= 2 && !flag)
	{
		if (!(nb % n))
			flag = 1;
		n--;
	}
	return (!flag);
}
