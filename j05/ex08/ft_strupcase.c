char	*ft_strupcase(char *str)
{
	char *tmp;

	tmp = str;
	while (*tmp != '\0')
	{
		if (*tmp >= 'a' && *tmp <= 'z')
			*tmp -= 32;
		tmp++;
	}
	return (str);
}
