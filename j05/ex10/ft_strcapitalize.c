char	*ft_strcapitalize(char *str)
{
	char	*tmp;

	tmp = str;
	if (*str == '\0')
		return (0);
	if (*str >= 'a' && *str <= 'z')
		*str -= 32;
	while (*(str + 1) != '\0')
	{
		if (*(str + 1) >= 'a' && *(str + 1) <= 'z')
			if (!(*(str) >= 'A' && *(str) <= 'Z')
			&& !(*(str) >= '0' && *(str) <= '9')
			&& !(*(str) >= 'a' && *(str) <= 'z'))
				*(str + 1) -= 32;
		str++;
	}
	return (tmp);
}
